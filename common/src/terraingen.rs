const TERRAIN_SURFACE_THICKNESS: f64 = 0.2;

pub struct VoronoiInfo {
    pub location: [f32; 2],
    pub block_id: BlockID,
}
impl VoronoiInfo {
    pub const fn new(block_id: BlockID, rain: f64, temp: f64) -> VoronoiInfo {
        VoronoiInfo {
            location: [rain as f32, temp as f32],
            block_id,
        }
    }

    // This function picks the material with the help of arrays of points in
    // 2D spaces defined by temp and rain with associated materials for each point.
    // The values of elev and dist determine which of eight arrays is chosen.
    // dist represents how far from the surface of the terrain a point is.
    // For values of dist between 0 and TERRAIN_SURFACE_THICKNESS, the four surface strata
    // arrays are used. Otherwise, the four general terrain strata arrays are used.
    // Variations between surface and terrain are intended to represent erosion and vegatation.
    // elev represents distance from the guiding plane. There are four strata of
    // terrain that are defined by elev thresholds. Variations between strata
    // are intended to represent the effects of more or less skylight being exposed.
    pub fn terraingen_voronoi(elev: f64, rain: f64, temp: f64, dist: f64) -> BlockID {
        let mut voxel_block: BlockID;

        let voronoi_choices = if dist <= TERRAIN_SURFACE_THICKNESS {
            if elev < -30.0 {
                SURFACE_DEEP
            } else if elev < -12.0 {
                SURFACE_LOW
            } else if elev < 2.0 {
                SURFACE_MED
            } else {
                SURFACE_HIGH
            }
        } else if elev < -30.0 {
            GENERAL_DEEP
        } else if elev < -12.0 {
            GENERAL_LOW
        } else if elev < 2.0 {
            GENERAL_MED
        } else {
            GENERAL_HIGH
        };

        let y: [f32; 2] = [rain as f32, temp as f32];
        let mut dist_squared = (voronoi_choices[0].location[0] - y[0]).powi(2)
            + (voronoi_choices[0].location[1] - y[1]).powi(2);
        voxel_block = voronoi_choices[0].block_id;
        for vc in voronoi_choices.iter().skip(1) {
            let d_squared = (vc.location[0] - y[0]).powi(2) + (vc.location[1] - y[1]).powi(2);
            if d_squared <= dist_squared {
                dist_squared = d_squared;
                voxel_block = vc.block_id;
            };
        }

        voxel_block
    }
}
